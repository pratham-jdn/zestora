import React, { useRef, useState } from "react";
import { IoArrowBack } from "react-icons/io5";
import { useDispatch, useSelector } from "react-redux";
import { useNavigate } from "react-router-dom";
import { FaUtensils } from "react-icons/fa";
import { serverUrl } from "../App";
import axios from "axios";
import { setMyShopData } from "../redux/ownerSlice";

const AddItem = () => {
  const navigate = useNavigate();
  const { myShopData } = useSelector((state) => state.owner);
  const [name,setName] = useState("")
  const [price,setPrice] = useState(0)
  const [frontendImage,setFrontendImage] = useState(null)
  const [backendImage,setBackendImage] = useState(null)
  const [category,setCategory] = useState("")
  const [foodType,setFoodType] = useState("Veg")
  const categories = ["Snacks",
            "Beverages",
            "Main Course",
            "Pizza",
            "Burger",
            "Desserts",
            "Salads",
            "Soups",
            "Appetizers",
            "Sides"]
  const dispatch = useDispatch()

  const handleImage=(e)=>{
    const file = e.target.files[0];
    setBackendImage(file);
    setFrontendImage(URL.createObjectURL(file));
  }

  const handleSubmit = async (e)=>{
    e.preventDefault();
    try {
      const formData = new FormData();
      formData.append("name", name);
      formData.append("category", category);
      formData.append("foodType", foodType);
      formData.append("price", price);
      if(backendImage){
        formData.append("image", backendImage);
      }
      const result = await axios.post(`${serverUrl}/api/item/add-item`, formData, {withCredentials: true});
      dispatch(setMyShopData(result.data))
      console.log("Item created/edited successfully:", result.data);
    } catch (error) {
      console.log("Error creating/editing item:", error);
    }
  }

  return (
    <div className="flex justify-center flex-col items-center p-6 bg-linear-to-br from-orange-50 relative to-white min-h-screen">
      <div
        className="absolute top-5 left-5 z-10 mb-2.5"
        onClick={() => navigate("/")}
      >
        <IoArrowBack size={25} className="text-[#ff4d2d]" />
      </div>

      <div className="max-w-lg w-full bg-white shadow-xl rounded-2xl p-8 border border-orange-100">
        <div className="flex flex-col items-center mb-6">
          <div className="bg-orange-100 p-4 rounded-full mb-4">
            <FaUtensils className="text-[#ff4d2d] w-16 h-16"/>
          </div>
          <div className="text-3xl font-extrabold text-gray-900">
            Add New Item
          </div>
        </div>
        <form className="space-y-5" onSubmit={handleSubmit}>
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">Name</label>
            <input type="text" placeholder="Enter Item Name" className="w-full px-4 py-2 border rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500"
              onChange={(e)=>setName(e.target.value)}
              value={name}
            />
          </div>
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">Item Image</label>
            <input type="file" accept='image/*' className="w-full px-4 py-2 border rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500" 
              onChange={(handleImage)}
            />
            {frontendImage && <div className="mt-4">
              <img src={frontendImage} alt="" className="w-full h-48 object-cover rounded-lg border"/>
            </div>}
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">Price</label>
            <input type="number" placeholder="0" className="w-full px-4 py-2 border rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500"
              onChange={(e)=>setPrice(e.target.value)}
              value={price}
            />
          </div>
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">Category</label>
            <select className="w-full px-4 py-2 border rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500"
              onChange={(e)=>setCategory(e.target.value)}
              value={category}
            >
                <option value="">Select a category</option>
                {categories.map((cat, index) => (
                  <option key={index} value={cat}>
                    {cat}
                  </option>
                ))}
            </select>
          </div>
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">Food Type</label>
            <select className="w-full px-4 py-2 border rounded-lg focus:outline-none focus:ring-2 focus:ring-orange-500"
              onChange={(e)=>setFoodType(e.target.value)}
              value={foodType}
            >
               
                  <option value="Veg">Veg</option>
                  <option value="Non-Veg">Non-Veg</option>
                 
            </select>
          </div>
          <button className="w-full bg-[#ff4d2d] text-white px-6 py-3 rounded-lg font-semibold shadow-md hover:bg-orange-600 hover:shadow-lg transition-all duration-200 cursor-pointer">
            Save
          </button>
        </form>
      </div>
    </div>
  );
};

export default AddItem;
